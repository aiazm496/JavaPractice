public class Division {
    public static void main(String[] args) {
        double d1 = 3/2;
        System.out.println(d1);

        double d2 = 3/2.0;
        System.out.println(d2);

        double d3 = 3/2*1;
        System.out.println(d3);

        double d4 = 3/(2*1.0);  //always use brackets in divisor
        System.out.println(d4);



    }
}
